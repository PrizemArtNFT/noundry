describe("rle encoding and decoding", () => {
  test("rle encoding", () => {
    const colorIndexes = [0, 0, 0, 0, 0, 0, 1, 1];
    const zeroCount = 6;
    const oneCount = 2;
    expect(rleEncode(colorIndexes)).toBe(
      toHexByte(zeroCount) + toHexByte(0) + toHexByte(oneCount) + toHexByte(1)
    );
  });

  test("rle encoding count is at most 255", () => {
    const colorIndexes = new Array(260).fill(0);
    expect(rleEncode(colorIndexes)).toBe(
      toHexByte(255) + toHexByte(0) + toHexByte(5) + toHexByte(0)
    );
  });

  test("rle decoding", () => {
    const colorIndexes = [0, 0, 0, 0, 0, 0, 1, 1];
    const zeroCount = 6;
    const oneCount = 2;
    expect(
      rleDecode(
        toHexByte(zeroCount) + toHexByte(0) + toHexByte(oneCount) + toHexByte(1)
      )
    ).toStrictEqual(colorIndexes);
  });

  test("rle decoding when count is over 255", () => {
    expect(
      rleDecode(toHexByte(255) + toHexByte(0) + toHexByte(5) + toHexByte(0))
    ).toStrictEqual(new Array(260).fill(0));
  });
});

describe("pixel packing and unpacking", () => {
  test("pack pixels", () => {
    const pixels = [
      [0, 0, 0],
      [0, 1, 0],
      [0, 0, 0],
    ].flat();
    const width = 3;
    const height = 3;

    expect(packToBoundedPixels(pixels, width, height)).toStrictEqual({
      bounds: { top: 1, right: 2, bottom: 1, left: 1 },
      pixels: [1],
    });
  });

  test("unpack pixels", () => {
    const pixels = [0, 0, 0, 0, 1, 0, 0, 0, 0];
    const width = 3;
    const height = 3;

    expect(
      unpackBoundedPixels(
        {
          bounds: { top: 1, right: 1, bottom: 1, left: 1 },
          pixels: [1],
        },
        width,
        height
      )
    ).toStrictEqual(pixels);
  });
});

